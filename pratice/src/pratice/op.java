package pratice;

public class op {

    public static void main(String args[]) {

        int a = 10;
        int b = 010;     // Octal = 8
        int c = 0X111;   // Hexadecimal = 273

        a += 10;         // a = a + 10

        System.out.println(a + b + c);
        System.out.println(a);
    }
}